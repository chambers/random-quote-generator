$(document).ready(function() {
    function getRandomQuote() {
        var randomItem = quotes[Math.floor(Math.random() * quotes.length)];
        var html = "<div class=\"quote\"><p id=\"epigraph\">\"" + randomItem.Epigraph + "\" --" + randomItem["Attributed to"] + "</p><p>Season " + randomItem.Season + ", " + randomItem.Episode + "</p></div>";
        $(".quote").html(html);
    }
    getRandomQuote();
    $("#getQuote").on("click", function() {
        getRandomQuote();
    });

});

$("#tweetThis").on("click", function() {
    var params = {
        text: "",
        related: "The Wire",
        hashtags: "theWire"
    };
    params.text = $("#epigraph").text();
    var encodedParams = $.param(params);
    var url = "https://twitter.com/intent/tweet?" + encodedParams;
    window.open(url, "_blank");
});
